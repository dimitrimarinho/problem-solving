#define MAXN 100001
char s[MAXN];
int p[MAXN];

int main( int argc, char* argv[] ){
	
	p[0] = p[1] = 0;
	while( true ){
		s[0] = getchar_unlocked();
		if( s[0] == '*' ) break;
		int i = 1;
		while( (s[i]=getchar_unlocked()) != '\n' ){
			int j = p[i];
			while( s[i] != s[j] ){
				if( j == 0 ){ j = -1; break; }
				j = p[j];
			}
			p[++i] = ++j;
		}
		int root = i/(i-p[i]);
		if( root*(i-p[i]) != i ) root = 1;
		printf("%d\n", root );
	}		
	
	return 0;
}




















